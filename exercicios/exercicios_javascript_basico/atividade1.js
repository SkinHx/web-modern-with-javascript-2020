// 01) Crie uma função que dado dois valores (passados como parâmetros) mostre no console a sum, subtração, 
// multiplicação e divisão desses valores.


function values(a, b) {
    var sum = a + b
    var subtraction = a - b
    var multiplication = a * b
    var division = a / b

    console.log(sum)
    console.log(subtraction)
    console.log(multiplication)
    console.log(division)
}

values(7, 16)
values(5, 4)